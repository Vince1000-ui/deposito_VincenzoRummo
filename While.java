public class While {
    public static void main(String[] args) {
    int i = 1; // Inizializzazione della variabile di controllo
while (i <= 5) {
     i++; // Incremento della variabile di controllo
     System.out.println(i);
}
 i = 1; // Inizializzazione della variabile di controllo
do {
    i++; // Incremento della variabile di controllo
    System.out.println(i);
} while (i <= 5);
}}
